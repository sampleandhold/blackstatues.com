jQuery.fn.devkScrollTo = function(parameter){
	this.bind('click', function (event) {
		var ziel = $(this).attr("href");
		$('html, body').stop().animate({scrollTop: $(ziel).offset().top}, parameter.speed, parameter.easing, function () {location.hash = ziel;});
		event.preventDefault();
	});
};


$(document).ready(function() {
	$("nav ul a").devkScrollTo({
		speed: "2000",
		easing: "easeInOutExpo"
	});
});

